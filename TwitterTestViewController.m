- (void)viewDidLoad {
    [super viewDidLoad];
	if (twitterEngine)
	{
		return;
	}
	twitterEngine = [[SA_OAuthTwitterEngine alloc] initOAuthWithDelegate: self];
	[twitterEngine setConsumerKey:kOAuthConsumerKey secret:kOAuthConsumerSecret];
	
	UIViewController *controller = [SA_OAuthTwitterController 
									controllerToEnterCredentialsWithTwitterEngine:twitterEngine delegate: self];
	
	if (controller) 
	{
		[self presentModalViewController: controller animated: YES];
	}
}


